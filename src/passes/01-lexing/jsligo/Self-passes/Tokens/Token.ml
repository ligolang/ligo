    | Do          t
    | Match       t
    | ParameterOf t
    | When        t -> [t#payload]

    (* Virtual tokens *)

    | ZWSP _
    | PARAMS _
    | ES6FUN _ -> [""]
    | SEMI_ELSE (l1,l2) -> [l1#payload; l2#payload]

    (* End-Of-File *)

    | EOF _ -> [""]


