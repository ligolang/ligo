    | PARAMS    of lexeme Wrap.t  (* Mark function's parameters *)
    | ES6FUN    of lexeme Wrap.t  (* Mark candidate lambda *)
    | SEMI_ELSE of (lexeme Wrap.t * lexeme Wrap.t) (* ; else *)

    (* End-Of-File *)

    | EOF of lexeme Wrap.t


