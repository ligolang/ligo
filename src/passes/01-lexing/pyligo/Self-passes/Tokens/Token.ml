    | OR_EQ      t
    | XOR_EQ     t
    | RSHIFT_EQ  t
    | LSHIFT_EQ  t
    | EXP_EQ     t
    | WILD       t
    | GRAVE      t
    | BACKSLASH  t

    (* Keywords *)

    | And      t
(*  | As       t *)
    | Assert   t
(*  | Async    t
    | Await    t
    | Break    t *)
    | Case     t
    | Class    t
(*  | Continue t *)
    | Const    t
    | Def      t
(*  | Del      t *)
    | Elif     t
    | Else     t
(*  | Except   t
    | Finally  t *)
    | For      t
(*  | From     t
    | Global   t *)
    | If       t
    | In       t
(*  | Is       t
    | Import   t *)
    | Lambda   t
    | Match    t
    | Module   t
(*  | Nonlocal t *)
    | Not      t
    | Or       t
    | Pass     t
(*  | Raise    t *)
    | Return   t
(*  | Try      t *)
    | Type     t
    | Var      t
    | While    t
(*  | With     t
    | Yield    t *)
      -> t#payload

    (* Virtual tokens *)

    | BEGIN     _
    | END       _  -> ""
    | INDENT    w -> String.make w#payload ' '
    | TOP_LEVEL _ -> ""
    | ZWSP      _ -> ""

    (* End-Of-File *)

    | EOF _ -> ""


